    // 8. SECURITY AUDIT
    public static class AuditEntry {
        public String date, user, action, details, role;
        public AuditEntry(String d, String u, String a, String det, String r) { date=d; user=u; action=a; details=det; role=r; }
    }

    public static void generateBeautifulAuditReport(Context context, String communityName, List<AuditEntry> logs, String reportRange) {
        try {
            File path = Environment.getExternalStoragePublicDirectory(Environment.DIRECTORY_DOWNLOADS); 
            File file = new File(path, getFormattedFileName(communityName, "Security_Audit"));
            PdfWriter writer = new PdfWriter(file.getAbsolutePath()); Document document = new Document(new PdfDocument(writer));
            
            addSanataniHeader(context, document, communityName, "SECURITY AUDIT & ACTIVITY LOG");
            document.add(new Paragraph(reportRange).setFontSize(12).setTextAlignment(TextAlignment.CENTER).setMarginBottom(15f));
            
            if (logs.isEmpty()) { 
                document.add(new Paragraph("No activity recorded for this period.").setTextAlignment(TextAlignment.CENTER)); 
            } else {
                List<AuditEntry> adminLogs = new ArrayList<>(); List<AuditEntry> managerLogs = new ArrayList<>();
                for (AuditEntry log : logs) {
                    if ("ADMIN".equals(log.role)) adminLogs.add(log); else managerLogs.add(log);
                }

                // 🟥 SUPER ADMIN SECTION
                document.add(new Paragraph("🛡️ SUPER ADMIN ACTIVITY").setBold().setFontSize(14).setFontColor(SAFFRON).setBackgroundColor(new DeviceRgb(255, 243, 224)).setPadding(4f));
                Table adminTable = new Table(UnitValue.createPercentArray(new float[]{2, 2, 4})).useAllAvailableWidth().setMarginBottom(15f);
                adminTable.addHeaderCell(new Cell().setBackgroundColor(GREY_BG).add(new Paragraph("Date").setBold().setFontSize(10)));
                adminTable.addHeaderCell(new Cell().setBackgroundColor(GREY_BG).add(new Paragraph("User").setBold().setFontSize(10)));
                adminTable.addHeaderCell(new Cell().setBackgroundColor(GREY_BG).add(new Paragraph("Action & Details").setBold().setFontSize(10)));

                if (adminLogs.isEmpty()) adminTable.addCell(new Cell(1,3).add(new Paragraph("No Admin actions recorded.").setItalic().setFontSize(10)));
                for (AuditEntry entry : adminLogs) {
                    adminTable.addCell(new Cell().add(new Paragraph(entry.date).setFontSize(9)));
                    adminTable.addCell(new Cell().add(new Paragraph(entry.user).setFontSize(9)));
                    adminTable.addCell(new Cell().add(new Paragraph(entry.action + "\n" + entry.details).setFontSize(9)));
                }
                document.add(adminTable);

                // 🟦 MANAGER SECTION
                document.add(new Paragraph("👥 MANAGER ACTIVITY").setBold().setFontSize(14).setFontColor(BLUE).setBackgroundColor(new DeviceRgb(227, 242, 253)).setPadding(4f));
                Table managerTable = new Table(UnitValue.createPercentArray(new float[]{2, 2, 4})).useAllAvailableWidth();
                managerTable.addHeaderCell(new Cell().setBackgroundColor(GREY_BG).add(new Paragraph("Date").setBold().setFontSize(10)));
                managerTable.addHeaderCell(new Cell().setBackgroundColor(GREY_BG).add(new Paragraph("User").setBold().setFontSize(10)));
                managerTable.addHeaderCell(new Cell().setBackgroundColor(GREY_BG).add(new Paragraph("Action & Details").setBold().setFontSize(10)));

                if (managerLogs.isEmpty()) managerTable.addCell(new Cell(1,3).add(new Paragraph("No Manager actions recorded.").setItalic().setFontSize(10)));
                for (AuditEntry entry : managerLogs) {
                    managerTable.addCell(new Cell().add(new Paragraph(entry.date).setFontSize(9)));
                    managerTable.addCell(new Cell().add(new Paragraph(entry.user).setFontSize(9)));
                    managerTable.addCell(new Cell().add(new Paragraph(entry.action + "\n" + entry.details).setFontSize(9)));
                }
                document.add(managerTable);
            }
            
            document.add(new Paragraph("\n[End of Official Audit Record]").setItalic().setFontSize(9).setTextAlignment(TextAlignment.CENTER).setFontColor(new DeviceRgb(150,150,150)));
            document.close(); shareFile(context, file, communityName + " - Security Audit");
        } catch (Exception e) {}
    }

    private static void shareFile(Context context, File file, String subject) {
        Uri uri = FileProvider.getUriForFile(context, context.getPackageName() + ".provider", file);
        Intent shareIntent = new Intent(Intent.ACTION_SEND); shareIntent.setType("application/pdf");
        shareIntent.putExtra(Intent.EXTRA_STREAM, uri); shareIntent.putExtra(Intent.EXTRA_SUBJECT, subject);
        shareIntent.addFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION);
        context.startActivity(Intent.createChooser(shareIntent, "Share PDF via..."));
    }
