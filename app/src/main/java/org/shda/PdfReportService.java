    private static String getFormattedFileName(String communityName, String reportType) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyyMMdd_HHmm", Locale.getDefault());
        String safeCommunity = communityName.replaceAll("[^a-zA-Z0-9]", "");
        String safeReport = reportType.replaceAll("[^a-zA-Z0-9]", "_");
        return safeCommunity + "_" + safeReport + "_" + sdf.format(new Date()) + ".pdf";
    }

    private static void addSanataniHeader(Context context, Document document, String communityName, String reportType) {
        document.add(new Paragraph(communityName.toUpperCase())
                .setFontColor(SAFFRON).setBold().setFontSize(24).setTextAlignment(TextAlignment.CENTER));
        document.add(new Paragraph(ShlokaEngine.getPdfSafeShloka(context))
                .setFontSize(11).setItalic().setFontColor(new DeviceRgb(117, 117, 117)).setTextAlignment(TextAlignment.CENTER));
        document.add(new Paragraph(reportType)
                .setBold().setFontSize(14).setBackgroundColor(GREY_BG).setTextAlignment(TextAlignment.CENTER).setMarginTop(8f).setPadding(4f));
        document.add(new Paragraph("Generated: " + new SimpleDateFormat("dd MMM yyyy, hh:mm a", Locale.getDefault()).format(new Date()))
                .setFontSize(10).setItalic().setTextAlignment(TextAlignment.CENTER).setMarginBottom(15f));
    }

    private static void addFooterSignature(Document document, String generatedBy) {
        document.add(new Paragraph("\nAuthorized & Generated By: " + generatedBy)
                .setFontSize(9).setItalic().setFontColor(new DeviceRgb(150, 150, 150)).setTextAlignment(TextAlignment.RIGHT).setMarginTop(20f));
    }

